package halo.model;


public class ServiceRecord {

   private String id;
   private int resultCode;
   
   public String getId() {
      return id;
   }
   
   public void setId( String id ) {
      this.id = id;
   }
   
   public int getResultCode() {
      return resultCode;
   }
   
   public void setResultCode( int resultCode ) {
      this.resultCode = resultCode;
   }
   
}
